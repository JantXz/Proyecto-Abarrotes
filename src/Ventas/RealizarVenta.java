    private void btnAniadirProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAniadirProductoActionPerformed
        
        String nombre = txtId.getText();
        String kilo="Ki";
        String iden = nombre.substring(0,2);
        
        if (Double.valueOf(txtCantidadDisp.getText()) < Double.valueOf(txtCantidad.getText())){
            
            JOptionPane.showMessageDialog(null, "El numero disponible es menor al requerido", "Error", HEIGHT);
            
        } else {
            
            if (!iden.equals(kilo)){
                
                productoSelecionadoPieza();
                Nuevo();
                consultarPiezas();
        } else {
                
                productoSelecionadoKilo();
                Nuevo();
                consultarKilos();
            }
            
        }
        
    }//GEN-LAST:event_btnAniadirProductoActionPerformed


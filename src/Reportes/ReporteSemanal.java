    void consultar(){
        String sql = "SELECT * FROM inventario.reportesemanales;";
        
        try {
            
            conet = con1.getConnection();
            st = conet.createStatement();
            rs = st.executeQuery(sql);
            
            Object[] datos = new Object[7];
            modelo = (DefaultTableModel) Tabla.getModel();
            
            while (rs.next()){
                
                datos [0] = rs.getString("id_Reporte");
                datos [1] = rs.getString("dia");
                datos [2] = rs.getString("mes");
                datos [3] = rs.getInt("anio");
                datos [4] = rs.getInt("ganancias");
                datos [5] = rs.getString("gastos");
                datos [6] = rs.getInt("ganancia_total");
                
                modelo.addRow(datos);
             
            }
            
            Tabla.setModel(modelo);
            
        }catch (Exception e){
            
        }
    } //fin consultar


    private void TablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaMouseClicked
        
        int fila = Tabla.getSelectedRow();
        if (fila == -1){
            
            JOptionPane.showMessageDialog(null, "No se seleciono fila");
            
        } else {
            
            String id = (String) Tabla.getValueAt (fila, 0);
            int dia = Integer.parseInt((String)Tabla.getValueAt(fila,1) .toString());
            int mes = Integer.parseInt((String)Tabla.getValueAt(fila,2) .toString());
            int anio = Integer.parseInt((String)Tabla.getValueAt(fila,3) .toString());
            float ganancias = Integer.parseInt((String)Tabla.getValueAt(fila,6) .toString());
            
            txtId.setText(""+id);
            txtDia.setText(""+dia);
            txtMes.setText(""+mes);
            txtAnio.setText(""+anio);
            txtGananciaTotal.setText(""+ganancias);
            
        }
        
    }//GEN-LAST:event_TablaMouseClicked


    private void Tabla1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Tabla1MouseClicked
        
        
        int fila = Tabla1.getSelectedRow();
        if (fila == -1){
            
            JOptionPane.showMessageDialog(null, "No se seleciono fila");
            
        } else {
            
            String id = (String) Tabla1.getValueAt(fila, 0);
            String nomPro = (String) Tabla1.getValueAt(fila, 1);
            float kilos = Integer.parseInt((String)Tabla1.getValueAt(fila,2) .toString());
            float precio = Integer.parseInt((String)Tabla1.getValueAt(fila,3) .toString());
            String tipoProducto = (String) Tabla1.getValueAt(fila, 4);
           
            
            txtId.setText(""+id);
            txtNombre.setText(""+nomPro);
            txtCantidadDisp.setText(""+kilos);
            txtPrecio.setText(""+precio);
            txtProducto.setText(""+tipoProducto);
            
        }
        
        Tabla1.clearSelection();
        
    }//GEN-LAST:event_Tabla1MouseClicked

